
describe('Sumas',()=> {
    // Suma de positivo con positivo debe ser positivo
    it('Suma de positivo con positivo debe ser positivo', () => {
        console.log('2 + 2');
        console.log('4');
    });
    // Suma de n1 positivo y > n2 que es negativo debe ser positivo 
    it('Suma de n1 positivo y > n2 que es negativo debe ser positivo', () => {
        console.log('3 + (-2)');
        console.log('1');
    });
});